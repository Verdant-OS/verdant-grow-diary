#!/usr/bin/env node
// Demo-Proof local helper: create a MINIMAL synthetic artifact set for
// validating local helper scripts. This is NOT a real Playwright report
// and does NOT prove the Demo Proof Walkthrough E2E passed.
//
// Creates:
//   .artifacts/demo-proof-playwright-report/index.html
//   .artifacts/demo-proof-playwright-report/data/app.js
//   .artifacts/demo-proof-playwright-report/assets/report.json
//   .artifacts/demo-proof-playwright-results/demo-proof-synthetic/trace.zip   (placeholder)
//   .artifacts/demo-proof-playwright-results/demo-proof-synthetic/screenshot.png (1x1 png)
//   .artifacts/demo-proof-playwright-results/demo-proof-synthetic/video.webm   (placeholder)
import { writeFileSync, mkdirSync } from "node:fs";
import { resolve, join } from "node:path";

const ROOT = process.cwd();
const REPORT = resolve(ROOT, ".artifacts/demo-proof-playwright-report");
const RESULTS = resolve(ROOT, ".artifacts/demo-proof-playwright-results");
const RESULT_CASE = join(RESULTS, "demo-proof-synthetic");

function ensure(dir) {
  mkdirSync(dir, { recursive: true });
}

ensure(REPORT);
ensure(join(REPORT, "data"));
ensure(join(REPORT, "assets"));
ensure(RESULT_CASE);

const created = [];

function write(path, data) {
  writeFileSync(path, data);
  created.push(path);
}

write(
  join(REPORT, "index.html"),
  `<!doctype html>
<html><head><meta charset="utf-8"><title>SYNTHETIC Demo-Proof report (local helper validation only)</title></head>
<body><h1>SYNTHETIC — NOT a real Playwright report</h1>
<p>Generated by scripts/create-synthetic-demo-proof-artifacts.mjs for local helper-script validation only.</p>
</body></html>
`,
);
write(
  join(REPORT, "data", "app.js"),
  `// SYNTHETIC placeholder for Demo-Proof local helper validation only.
// Not a real Playwright report asset.
`,
);
write(
  join(REPORT, "assets", "report.json"),
  JSON.stringify({ synthetic: true, note: "local helper validation only" }, null, 2),
);

// Placeholder trace.zip — clearly labeled. NOT openable by playwright show-trace.
write(
  join(RESULT_CASE, "trace.zip"),
  "SYNTHETIC-PLACEHOLDER: not a real Playwright trace. For local file-discovery validation only.\n",
);

// 1x1 transparent PNG
const onePxPng = Buffer.from(
  "89504E470D0A1A0A0000000D49484452000000010000000108060000001F15C4890000000D49444154789C636000010000050001" +
    "0D0A2DB40000000049454E44AE426082",
  "hex",
);
write(join(RESULT_CASE, "screenshot.png"), onePxPng);

write(
  join(RESULT_CASE, "video.webm"),
  "SYNTHETIC-PLACEHOLDER: not a real webm video. For local file-discovery validation only.\n",
);

console.log("Synthetic Demo-Proof artifacts created (helper-validation only):");
for (const p of created) console.log("  " + p);
console.log("");
console.log("WARNING: These files validate local helper behavior only.");
console.log("They do NOT prove the Demo Proof Walkthrough E2E passed.");
console.log("Use GitHub Actions artifacts for real proof.");
process.exit(0);
